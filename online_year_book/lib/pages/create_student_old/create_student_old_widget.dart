                      final firestoreBatch = FirebaseFirestore.instance.batch();
                      try {
                        if (_model.formKey.currentState == null ||
                            !_model.formKey.currentState!.validate()) {
                          return;
                        }
                        if (_model.sectionDropdownValue == null) {
                          return;
                        }
                        if (_model.yearGraduatedDropdownValue == null) {
                          return;
                        }
                        if (_model.courseDropdownValue == null) {
                          return;
                        }
                        _model.studentNameValid =
                            await actions.isStudentNameValid(
                          _model.firstNameEditController.text,
                          _model.middleNameEditController.text,
                          _model.lastNameEditController.text,
                          widget.studentToEdit,
                        );
                        if (_model.studentNameValid == true) {
                          if (widget.studentToEdit == null) {
                            final studentsCreateData = createStudentsRecordData(
                              photoUrl: _model.uploadedFileUrl,
                              firstName: _model.emailEditController.text,
                              middleName: _model.middleNameEditController.text,
                              lastName: _model.lastNameEditController.text,
                              yearGraduated: _model.selectedYear,
                              section: _model.selectedSection,
                              course: _model.selectedCourse,
                            );
                            firestoreBatch.set(StudentsRecord.collection.doc(),
                                studentsCreateData);
                          } else {
                            final studentsUpdateData = createStudentsRecordData(
                              photoUrl: _model.uploadedFileUrl,
                              firstName: _model.firstNameEditController.text,
                              middleName: _model.middleNameEditController.text,
                              lastName: _model.lastNameEditController.text,
                              section: _model.selectedSection,
                              course: _model.selectedCourse,
                              yearGraduated: _model.selectedYear,
                            );
                            firestoreBatch.update(
                                widget.studentToEdit!.reference,
                                studentsUpdateData);
                          }

                          ScaffoldMessenger.of(context).showSnackBar(
                            SnackBar(
                              content: Text(
                                'Saved Successfully',
                                style: TextStyle(
                                  color:
                                      FlutterFlowTheme.of(context).primaryText,
                                ),
                              ),
                              duration: Duration(milliseconds: 4000),
                              backgroundColor:
                                  FlutterFlowTheme.of(context).secondary,
                            ),
                          );

                          context.goNamed(
                            'studentsPage',
                            extra: <String, dynamic>{
                              kTransitionInfoKey: TransitionInfo(
                                hasTransition: true,
                                transitionType: PageTransitionType.leftToRight,
                              ),
                            },
                          );
                        } else {
                          ScaffoldMessenger.of(context).showSnackBar(
                            SnackBar(
                              content: Text(
                                'Student name is already exist!',
                                style: TextStyle(
                                  color:
                                      FlutterFlowTheme.of(context).primaryText,
                                ),
                              ),
                              duration: Duration(milliseconds: 4000),
                              backgroundColor:
                                  FlutterFlowTheme.of(context).secondary,
                            ),
                          );
                        }
                      } finally {
                        await firestoreBatch.commit();
                      }

                      setState(() {});
                    },
                    text: FFLocalizations.of(context).getText(
                      'pw7fkz2i' /* Save Changes */,
                    ),
                    options: FFButtonOptions(
                      width: 270.0,
                      height: 50.0,
                      padding:
                          EdgeInsetsDirectional.fromSTEB(0.0, 0.0, 0.0, 0.0),
                      iconPadding:
                          EdgeInsetsDirectional.fromSTEB(0.0, 0.0, 0.0, 0.0),
                      color: FlutterFlowTheme.of(context).primary,
                      textStyle: FlutterFlowTheme.of(context)
                          .titleMedium
                          .override(
                            fontFamily:
                                FlutterFlowTheme.of(context).titleMediumFamily,
                            color: FlutterFlowTheme.of(context).primaryBtnText,
                            useGoogleFonts: GoogleFonts.asMap().containsKey(
                                FlutterFlowTheme.of(context).titleMediumFamily),
                          ),
                      elevation: 2.0,
                      borderSide: BorderSide(
                        color: Colors.transparent,
                        width: 1.0,
                      ),
                      borderRadius: BorderRadius.circular(50.0),
                    ),
                  ),
                ),
              ),
            ],
          ),
        ),
      ),
    );
  }
